public class A4_Song {

    private String typeOfPlaylist;
    private String nameOfSong;
    private String timeOfSong;

    public void setTypeOfPlaylist(String typeOfPlaylist) {
        this.typeOfPlaylist = typeOfPlaylist;
    }

    public void setNameOfSong(String nameOfSong) {
        this.nameOfSong = nameOfSong;
    }

    public void setTimeOfSong(String timeOfSong) {
        this.timeOfSong = timeOfSong;
    }

    public String getTypeOfPlaylist() {
        return typeOfPlaylist;
    }

    public String getNameOfSong() {
        return nameOfSong;
    }

    public String getTimeOfSong() {
        return timeOfSong;
    }
}
